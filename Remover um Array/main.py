cores = ["Azul", "Verde", "Vermelho"]
print(cores)
"O pop serve para excluir um elemento da minha lista"
item = cores.pop()
"Ficou só Azul e Verde"
print (cores)
"Ficou somente a última cor da lista"
print(item)
"também podemos fazer o seguinte, utilizar apenas cores.pop() e print(cores) e vai aparecer somente as duas primeiras cores da lista, Azul e Verde."
